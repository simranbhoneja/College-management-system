create table Student(Stud_id varchar(15) primary key, Stud_name varchar(40), Gender varchar(1), Class varchar, Sem int, Branch varchar(30), Course_id varchar(15));
select * from Student;
alter table Student alter column Class varchar(5);
create table Course(Course_id varchar(15) primary key, Course_name varchar(30));
select * from Course;
alter table Student add constraint FK_Course_id foreign key(Course_id) references Course(Course_id);
create table Teacher(Prof_id varchar(15) primary key, Prof_name varchar(40), Gender varchar(1), Education varchar(30), Teaching_exp int);
select * from Teacher
create table Subject(Subj_id varchar(15) primary key, subj_name varchar(30), Sem int, Branch varchar(30), Prof_id varchar(15) foreign key references Teacher(Prof_id)); 
select * from Subject;
create table Contain(Course_id varchar(15) foreign key references Course(Course_id),Subj_id varchar(15) foreign key references Subject(Subj_id),primary key(Course_id,Subj_id));
select * from Contain;
create table Examination(Exam_id varchar(15) primary key, Exam_date date);
select * from Examination;
create table Gives(Stud_id varchar(15) foreign key references Student(Stud_id), Exam_id varchar(15) foreign key references Examination(Exam_id), Result float, primary key(Stud_id,Exam_id));
